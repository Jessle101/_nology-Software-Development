// Operators in JS:

// Addition +
// Subtraction -
// Multiplication *
// Division /
// Power of **
// Increment => ++ (increase by one)
// Decrement => -- (decrease by one)

let value = 124;
let result = value / 2;
result = result * 99;
result = result - 87 + 9;
result = result ** 3;

// ++ will increment value by one
console.log(result++); // 001
console.log(++result); // 002

console.log(result--); // -001
console.log(--result); // -002

const firstName = "John";
const lastName = "Wayne";
const fullName = firstName + " " + lastName;
console.log(fullName);
